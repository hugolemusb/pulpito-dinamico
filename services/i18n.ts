export const LANGUAGE_NAMES: Record<Language, string> = {
  es: 'Español',
  en: 'English',
  pt: 'Português'
};

export const getCurrentLanguage = (): Language => {
  return (sessionStorage.getItem('LOCKED_LANGUAGE') as Language) ||
    (localStorage.getItem('app_language') as Language) ||
    'es';
};

export const getAILanguageInstruction = (): string => {
  const lang = getCurrentLanguage();
  const name = LANGUAGE_NAMES[lang];

  return `IDIOMA DE SESIÓN: ${lang}
INSTRUCCIÓN CRÍTICA: Debes responder EXCLUSIVAMENTE en ${name}. 
- Si SELECTED_LANGUAGE = 'es': Responde en español
- Si SELECTED_LANGUAGE = 'en': Responde en inglés (English)
- Si SELECTED_LANGUAGE = 'pt': Responde en portugués (Português)

CONTEXTO: Esta es una aplicación bíblica. Cuando menciones libros bíblicos, capítulos, 
versículos o conceptos teológicos, usa la nomenclatura y terminología apropiada 
para ${name}.

NUNCA mezcles idiomas. NUNCA respondas en un idioma diferente al indicado.
Todas las citas bíblicas, referencias y explicaciones deben estar en ${name}.`;
};

